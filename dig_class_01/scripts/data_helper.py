# flatten function from scratch
def flatten(image: list[list]):
    flattened_img = []

    rows = len(image)

    for i in range(rows):
        flattened_img += image[i]

    return flattened_img


# length of a vector from scratch
def vector_length(x):
    len = 0

    for _ in x:
        len += 1

    return len


# calculate nonzero elements in a vector
def count_nonzero(x):
    nonzero = 0

    for element in x:
        if element != 0:
            nonzero += 1

    return nonzero


# normalize the flattened image vector
def normalize(x):
    normalized_img = []

    for element in x:
        format_element = round(element / 255, 3)
        normalized_img.append(format_element)

    return normalized_img


# function to determine valid label
def is_valid_label(label):
    if label >= 0 and label <= 9:
        return True
    else:
        return False


